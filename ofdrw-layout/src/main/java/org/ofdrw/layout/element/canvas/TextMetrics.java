package org.ofdrw.layout.element.canvas;

/**
 * 测量的文字信息
 *
 * @author 权观宇
 * @since 2020-05-07 19:03:17
 */
public class TextMetrics {

    /**
     * 文字方向
     */
    public int charDirection;

    /**
     * 阅读方向
     */
    public int readDirection;

    /**
     * 文字宽度
     */
    public double width;

    /**
     * 文字高度
     */
    public double height;
}
